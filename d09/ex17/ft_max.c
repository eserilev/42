int	ft_max(int *tab, int length)
{
	int l;
	int i;
	int max;

	i = 0;
	l = length;
	max = -2147483648;
	if (tab[0] = '\0')
		return (0);
	while (tab[i] != '\0')
	{
		if (max < tab[i])
		{
			max = tab[i];
		}
		i++;
	}
	return (max);
}
