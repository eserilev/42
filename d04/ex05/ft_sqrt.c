int	ft_sqrt(int nb)
{
	int i;
	int max_val;

	i = 0;
	if (nb == 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (nb);
	if (nb == 4)
		return (2);
	max_val = nb / 2;
	while (i <= max_val)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
