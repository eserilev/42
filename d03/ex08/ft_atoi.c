int	ft_atoi(char *str)
{
	int	i;
	int	temp[256];
	int	j;
	int	value;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			temp[i] = str[i] - '0';
		}
		else
			break ;
		i++;
	}
	while (j < i)
	{
		value = 10 * value + temp[j];
		j++;
	}
	return (value);
}
