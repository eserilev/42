char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	c[256];

	i = -1;
	j = 0;
	while (str[j] != '\0')
	{
		c[j] = str[j];
		i++;
		j++;
	}
	j = 0;
	while (i >= 0)
	{
		str[j] = c[i];
		j++;
		i--;
	}
	return (str);
}
