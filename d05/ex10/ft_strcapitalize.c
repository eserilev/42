char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0)
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] -= 32;
		}
		if (str[i] <= '0' || str[i] >= '9')
		{
			if (str[i] <= 'a' || str[i] >= 'z')
			{
				if (str[i] <= 'A' || str[i] >= 'Z')
				{
					if (str[i + 1] >= 'a' && str[i + 1] <= 'z')
						str[i + 1] -= 32;
				}
			}
		}
		i++;
	}
	return (str);
}
